    public void PreencheMinutos() {
        for (int i = 0; i <= 59; i++) {
            if (i <= 9) {
                edtInicioMin.addItem("0" + Integer.toString(i));
                edtFimMinutos.addItem("0" + Integer.toString(i));
            } else {
                edtInicioMin.addItem(Integer.toString(i));
                edtFimMinutos.addItem(Integer.toString(i));
            }
        }
    }

    public void PreencheHoras() {
        for (int i = 0; i <= 24; i++) {
            if (i <= 9) {
                edtInicioHora.addItem("0" + Integer.toString(i));
                edtFimHoras.addItem("0" + Integer.toString(i));
            } else {
                edtInicioHora.addItem(Integer.toString(i));
                edtFimHoras.addItem(Integer.toString(i));
            }
        }
    }

    public void preencherCampos(Motorista m) {
        edtNome.setText(m.getNome());
        edtCpf.setText(m.getCpf());
        edtDataNascimento.setDate(m.getDataNascimento());
        edtEmail.setText(m.getEmail());
        edtSexo.setText(m.getSexo());
        edtRemuneracaoMensal.setText(String.valueOf(m.getRemuneracaoMensal()));
        edtInicioHora.setSelectedIndex(m.getHoraDeEntrada().getHours());
        edtInicioMin.setSelectedIndex(m.getHoraDeEntrada().getMinutes());
        edtFimHoras.setSelectedIndex(m.getHoraDeSaida().getHours());
        edtFimMinutos.setSelectedIndex(m.getHoraDeSaida().getMinutes());
    }

    public void habDesCampos(boolean flag) {
        edtNome.setEnabled(flag);
        edtEmail.setEnabled(flag);
        edtInicioHora.setEnabled(flag);
        edtInicioMin.setEnabled(flag);
        edtFimHoras.setEnabled(flag);
        edtFimMinutos.setEnabled(flag);
        edtCpf.setEnabled(flag);
        edtSexo.setEnabled(flag);
        edtRemuneracaoMensal.setEnabled(flag);
        edtCarteira.setEditable(flag);
    }

    public void limparCampos() {
        edtNome.setText("");
        edtEmail.setText("");
        edtInicioHora.setSelectedIndex(0);
        edtInicioMin.setSelectedIndex(0);
        edtFimHoras.setSelectedIndex(0);
        edtFimMinutos.setSelectedIndex(0);
        edtCpf.setText("");
        edtSexo.setText("");
        edtRemuneracaoMensal.setText("");
        edtCarteira.setSelectedIndex(0);
        edtDataNascimento.setDate(new Date());
    }

    private Object getObjectSelectOnGrid() {
        int rowClicked = grdMotorista.getSelectedRow();
        Object obj = null;
        if (rowClicked >= 0) {
            obj = grdMotorista.getModel().getValueAt(rowClicked, -1);
        }
        return obj;
    }

